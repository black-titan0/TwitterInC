#define CYAN    "\x1b[36m"
#define RESET   "\x1b[0m"
#define GREY     "\x1b[90m"


void printASCIIart(char *art[] , int size , char color , int space)
{
    for (int i = 0; i < size; ++i) {
        if (color == 'C')
            printf(CYAN "%s" RESET, art[i]);
        else if (color == 'M')
            printf(MAGENTA "%s" RESET, art[i]);
        else if (color == 'R')
            printf(RED "%s" RESET, art[i]);
        else if (color == 'B')
            printf(BLUE "%s" RESET, art[i]);
        else if (color == 'W')
            printf(RESET "%s" RESET, art[i]);
        else if (color == 'G')
            printf(GREEN "%s" RESET, art[i]);
        else if (color == 'Y')
            printf(YELLOW "%s" RESET, art[i]);
        else if (color == 'E')
            printf(GREY "%s" RESET, art[i]);
    }

    for (int i = 0; i < space; ++i) {
        printf("\n");
    }

}
